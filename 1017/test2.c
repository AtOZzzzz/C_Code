/*  
    逗号表达式
    逗号运算符的优先级最低，比赋值运算符还低。
    表达式 1，表达式 2  --------------并以表达式 2 的值作为整个逗号表达式的值。
    拓展：表达式 1，(表达式 2，表达式 3) ------------------值为表达式3

*/

#include <stdio.h>

int main()
{

    int a=2,b=4,c=6,x,y;
    //y=(x=a+b),(b+c);  //x=6 ;y=6;  因为  y=(x=a+b)-->表达式1 ， (b+c)-->表达式2
    //printf("%d %d ",x,y);
    y=((x=a+b),(b+c));  //x=6 y=10;        (x=a+b)-->表达式1，(b+c)-->表达式2
    printf("%d %d ",x,y);


    return 0;
}